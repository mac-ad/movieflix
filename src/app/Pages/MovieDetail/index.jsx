import React, { useEffect, useState } from "react";
import { medium, original, small } from "../../statics/";
import { useLocation, useNavigate, useSearchParams } from "react-router-dom";

import "./MovieDetail.css";
import CastCrew from "./CastCrew";
import { useParams } from "react-router-dom";
import { axiosInstance } from "../../config/axios";
import { movieDetailUrl } from "../../utils/queryUrls";
import { CompanyLogo, Tag } from "../../components";
import Icon from "../../components/Icon";
import { ArrowLeft } from "../../icons";

const MovieDetail = () => {
  // const location = useLocation();
  const { movieName, movieId } = useParams();

  const [data, setData] = useState({});
  const [loading, setLoading] = useState(false);

  const navigate = useNavigate();

  const fetchData = async () => {
    setLoading(true);
    const res = await axiosInstance({
      method: "get",
      url: `${movieDetailUrl}${movieId}`,
    });
    setLoading(false);
    console.log(res.data);
    setData((prev) => res.data);
  };

  useEffect(() => {
    if (movieId) fetchData();
  }, [movieId]);

  useEffect(() => {
    console.log(movieId, movieName);
  }, []);

  if (loading) return <div>...loading</div>;

  return (
    <div className="movie-detail-page-container">
      <div
        className="poster-part-container"
        style={{
          backgroundImage: `linear-gradient(to bottom, rgba(0,0,0,.7), rgba(16, 16, 16,1)) , url(${small}${data?.backdrop_path})`,
        }}
      >
        <div className="main wrapper">
          <div className="detail">
            <div className="header">
              <div
                className="goback"
                onClick={(e) => {
                  navigate(-1);
                }}
              >
                <Icon icon={<ArrowLeft />} />
              </div>
              <h2 className="title">{data?.title}</h2>
              <ul className="brief secondary secondary-font">
                <li>{data?.release_date?.split("-")[0]}</li>
                <li>{data?.runtime}min</li>
              </ul>
            </div>
            <div className="detail-main">
              <div className="poster">
                <img src={`${original}${data?.poster_path}`} alt="" />
              </div>
              <div className="infos">
                <div className="genres">
                  {data?.genres?.map((genre) => (
                    <Tag key={genre.name} name={genre.name} />
                  ))}
                </div>
                <p className="overview secondary-text">{data?.overview}</p>
                <div className="production-companies">
                  {data?.production_companies?.map((company) =>
                    company.logo_path ? (
                      <CompanyLogo
                        key={company.name}
                        logoPath={company.logo_path}
                      />
                    ) : null
                  )}
                </div>
              </div>
            </div>
          </div>
        </div>
      </div>
      <CastCrew movieId={data.id} movieName={data.title} />
    </div>
  );
};

